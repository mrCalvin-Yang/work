#import "QuestionDeleteVM.h"

@implementation QuestionDeleteVM

+(void)questionDeleteWithQuestionId:(NSString *)questionid sucess:(void (^)(NSDictionary *))success fail:(void (^)(NSString *))fail{
    NSString *url = [NSString stringWithFormat:[PerinatalAppApi GetFormatURLString:URLTypeProblemDelete],questionid];
    if (url) {
        [HttpRequest sendDelete:url param:nil success:^(id respone) {
            success(respone);
        } fail:^(NSString *respone) {
            fail(respone);
        }];
    }
}

@end
