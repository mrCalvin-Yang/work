+ (NSString *)timeWithTimeIntervalString:(NSString *)timeString
{
    // 格式化时间
    NSDateFormatter* formatter = [[NSDateFormatter alloc] init];
    formatter.timeZone = [NSTimeZone timeZoneWithName:@"shanghai"];
    [formatter setDateStyle:NSDateFormatterMediumStyle];
    [formatter setTimeStyle:NSDateFormatterShortStyle];
    [formatter setDateFormat:@"yyyy-MM-dd HH:mm:ss"];
    
    // 毫秒值转化为秒
    NSDate* date = [NSDate dateWithTimeIntervalSince1970:[timeString doubleValue]/ 1000.0];
    NSString* dateString = [formatter stringFromDate:date];
    return dateString;
}

+(NSInteger)getTimeDifferenceWithString:(NSString *)time{
    if (![NSString isBlankString:time]) {
        NSInteger second = time.integerValue/1000;
        NSDate *tody = [NSDate date];
        NSInteger nowinterval = [tody timeIntervalSince1970];
        NSInteger difference = nowinterval - second;
        return difference;
    }
    return 0;
}

